    private void preencherCombobox() {
        conexao.conexao();
        conexao.executaSQL("select * from fornecedores order by nome_fornecedor");//conecta ao banco de dados
        jComboBoxForn.removeAllItems();
        try {
            //pega o primeiro dado do banco de dados como parametro
            conexao.rs.first();
            do {
                jComboBoxForn.addItem(conexao.rs.getString("nome_fornecedor")); //adiciona os dados ao combobox
            } while (conexao.rs.next());//faz o processo anterior enquanto houver proximo.
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher combobox Fornecedores  :" + ex);
        }

    }
    
    private void preencherComboboxUnd() {
        conexao.conexao();
        conexao.executaSQL("select * from unidades order by id_unidade");//conecta ao banco de dados
        jComboBox2.removeAllItems();
        try {
            //pega o primeiro dado do banco de dados como parametro
            conexao.rs.first();
            do {
              jComboBox2.addItem(conexao.rs.getString("unidade")); //adiciona os dados ao combobox
            } while (conexao.rs.next());//faz o processo anterior enquanto houver proximo.
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher combobox  :" + ex);
        }

        conexao.desconecta();

    }

    public void recebeDados(String codigo) {
        mod.setPesquisa(codigo);
        ModeloProduto model = control.buscaIdCadastro(mod);

        jTextFieldCodBar.setText(model.getCodebar());
        jTextFieldNome.setText(model.getNome());
        jTextFieldCod.setText(String.valueOf(model.getId()));
        jTextFieldCusto1.setText(String.valueOf(model.getPrecocompra()));
        jTextFieldPrecovenda.setText(String.valueOf(model.getPrecovenda()));
        jTextFieldQTD.setText(String.valueOf(model.getQuantidade()));
        jTextFieldRef.setText(model.getReferencia());
        jComboBoxForn.setSelectedItem(model.getFornecedor());
        jComboBox2.setSelectedItem(model.getUnidade());
        jTextFieldNCM.setText(model.getNcm());

    }

