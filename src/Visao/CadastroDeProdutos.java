    private void jButtonMarkupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMarkupActionPerformed
        try {
            custo = Float.parseFloat(jTextFieldCusto1.getText());
            markup = Float.parseFloat(jTextFieldLucro.getText());
            if (markup != 0) {
                preco = (custo + ((custo * markup) / 100));
                jTextFieldPrecovenda.setText(String.valueOf(preco));
                jLabelMensangemErroVenda.setText("");
                jLabelMensangemErroMarkup.setText("");
            } else {
                jTextFieldLucro.setText("");
            }
        } catch (NumberFormatException ex) {
            jLabelMensangemErroMarkup.setText("Substitua campos com virgula por um ponto '.'");

        }
    }//GEN-LAST:event_jButtonMarkupActionPerformed

