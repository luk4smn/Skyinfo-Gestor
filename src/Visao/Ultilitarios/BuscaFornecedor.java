   public void preencherTabelaForn(String SQL){
    ArrayList dados = new ArrayList ();
    String [] Colunas = new String[]{"ID","Fornecedor","Inscrição Estadual","CNPJ"};
    connforn.conexao();
    connforn.executaSQL(SQL);
   try {
        connforn.rs.first();
        do{
          dados.add(new Object[]{connforn.rs.getInt("id_fornecedor"), connforn.rs.getString("nome_fornecedor"),connforn.rs.getString("ie_forn"),connforn.rs.getString("cnpj_fornecedor")});
          }while(connforn.rs.next());
        } catch (SQLException ex){
        }
   ModeloTabela modelo = new ModeloTabela(dados,Colunas);
       
   jTablePesquisaForncedor.setModel(modelo);
   jTablePesquisaForncedor.getColumnModel().getColumn(0).setPreferredWidth(50);
   jTablePesquisaForncedor.getColumnModel().getColumn(0).setResizable(!false);
   jTablePesquisaForncedor.getColumnModel().getColumn(1).setPreferredWidth(280);
   jTablePesquisaForncedor.getColumnModel().getColumn(1).setResizable(true);
   jTablePesquisaForncedor.getColumnModel().getColumn(2).setPreferredWidth(130);
   jTablePesquisaForncedor.getColumnModel().getColumn(2).setResizable(!false);
   jTablePesquisaForncedor.getColumnModel().getColumn(3).setPreferredWidth(280);
   jTablePesquisaForncedor.getColumnModel().getColumn(3).setResizable(!false);


   jTablePesquisaForncedor.getTableHeader().setReorderingAllowed(true);
   jTablePesquisaForncedor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       
  
   }
   
