    private void jTablePesquisaForncedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePesquisaForncedorMouseClicked
        String nome_cliente =""+ jTablePesquisaForncedor.getValueAt(jTablePesquisaForncedor.getSelectedRow(),1);
        connforn.conexao();
        connforn.executaSQL("select * from fornecedores where nome_fornecedor like '"+nome_cliente+"'");

        try {
            connforn.rs.first();
            jTextFieldFornecedor.setText(connforn.rs.getString("nome_fornecedor"));

            connforn.desconecta();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog (null,"Erro ao Carregar da tabela !" +ex);

        }
    }//GEN-LAST:event_jTablePesquisaForncedorMouseClicked

    private void jButtonVincularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVincularActionPerformed
if (flagbusca<=1){
        recebe = new ModuloDeCompras();
        recebe.recebeDadosFornecedor(jTextFieldFornecedor.getText());
        recebe.setVisible(true);
        connforn.desconecta();
} else{
        recebebusca = new RelatorioProdutoPorFornecedor();
        recebebusca.recebeDadosForn(jTextFieldFornecedor.getText());
        recebebusca.setVisible(true);
        connforn.desconecta();
                    
                    } 
        
        dispose();
    }//GEN-LAST:event_jButtonVincularActionPerformed

