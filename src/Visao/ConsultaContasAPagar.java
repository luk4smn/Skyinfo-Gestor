  public void preencherTabela(String SQL){
      ArrayList dados = new ArrayList ();
    String [] Colunas = new String[]{"ID","Parcela","NF","Compra","Vencimento","Fornececdor","Valor da Parcela","Estado"};
    conn.conexao();
    conn.executaSQL(SQL);
   try {
        conn.rs.first();
        do{
          dados.add(new Object[]{conn.rs.getString("id_parc_compra"), conn.rs.getString("numero_parcela"),conn.rs.getString("nf_compra"),conn.rs.getString("id_compra"),conn.rs.getString("data"),conn.rs.getString("nome_fornecedor"),conn.rs.getString("valor_parcela"),conn.rs.getString("estado")});
          }while(conn.rs.next());
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
      ModeloTabela modelo = new ModeloTabela(dados,Colunas);
       
   Tabela.setModel(modelo);
   Tabela.getColumnModel().getColumn(0).setPreferredWidth(50);
   Tabela.getColumnModel().getColumn(0).setResizable(!false);
   Tabela.getColumnModel().getColumn(1).setPreferredWidth(100);
   Tabela.getColumnModel().getColumn(1).setResizable(true);
   Tabela.getColumnModel().getColumn(2).setPreferredWidth(100);
   Tabela.getColumnModel().getColumn(2).setResizable(!false);
   Tabela.getColumnModel().getColumn(3).setPreferredWidth(100);
   Tabela.getColumnModel().getColumn(3).setResizable(!false);
   Tabela.getColumnModel().getColumn(4).setPreferredWidth(130);
   Tabela.getColumnModel().getColumn(4).setResizable(!false);
   Tabela.getColumnModel().getColumn(5).setPreferredWidth(130);
   Tabela.getColumnModel().getColumn(5).setResizable(!false);
   Tabela.getColumnModel().getColumn(6).setPreferredWidth(130);
   Tabela.getColumnModel().getColumn(6).setResizable(!false);

   Tabela.getTableHeader().setReorderingAllowed(true);
   Tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
  
   } 
    
    
