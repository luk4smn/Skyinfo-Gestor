    private void TabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaMouseClicked
        idparcela =""+ Tabela.getValueAt(Tabela.getSelectedRow(),0);
        conn.conexao();
        conn.executaSQL("select * from contas_pagar inner join compra on contas_pagar.id_compra= compra.id_compra inner join fornecedores on compra.id_fornecedor = fornecedores.id_fornecedor where id_parc_compra ="+idparcela);

        try {
            conn.rs.first();
            jTextFieldIDCompra.setText(String.valueOf(conn.rs.getInt("id_compra")));
            jTextFieldData.setText(conn.rs.getString("data"));
            jTextFieldValorCompra.setText(conn.rs.getString("valor_total"));
            jTextFieldParcelasValor.setText(conn.rs.getString("valor_parcela"));
            jTextFieldEstado.setText(conn.rs.getString("estado"));
            jTextFieldParcela.setText(String.valueOf(conn.rs.getInt("numero_parcela")));
            jTextFieldNF.setText(conn.rs.getString("nf_compra"));
            jTextFieldForn.setText(conn.rs.getString("nome_fornecedor"));
            conn.desconecta();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog (null,"Erro ao Carregar da tabela !" +ex);

        }        // TODO add your handling code here:
    }//GEN-LAST:event_TabelaMouseClicked

