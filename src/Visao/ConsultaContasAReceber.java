    private void TabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaMouseClicked
        idparcela = "" + Tabela.getValueAt(Tabela.getSelectedRow(), 0);
        conn.conexao();
        conn.executaSQL("select * from contas_receber inner join venda on contas_receber.id_venda= venda.id_venda inner join clientes on venda.id_cliente = clientes.id_cliente where id_parc_venda =" + idparcela);

        try {
            conn.rs.first();
            jTextFieldIDVenda.setText(String.valueOf(conn.rs.getInt("id_venda")));
            jTextFieldData.setText(conn.rs.getString("data"));
            jTextFieldValorvenda.setText(conn.rs.getString("valor_total"));
            jTextFieldParcelasValor.setText(conn.rs.getString("valor_parcela"));
            jTextFieldEstado.setText(conn.rs.getString("estado"));
            jTextFieldParcela.setText(String.valueOf(conn.rs.getInt("numero_parcela")));
            jTextFieldCli.setText(conn.rs.getString("nome_cliente"));
            conn.desconecta();
            jButtonBaixar.setEnabled(true);
            jButtonCnacelarBaixa.setEnabled(true);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Carregar da tabela !" + ex);

        }        // TODO add your handling code here:
    }//GEN-LAST:event_TabelaMouseClicked

